    public void cargarCombo()
    {
        lista = daoE.mostrarCarrera();
        for(int i=0;i<lista.size();i++)
        {
            cmb = new Combo(lista.get(i).getIdCombo(),lista.get(i).getDescripcion());
            this.jCmbEscuela.addItem(cmb.toString());    
        }    
    }
    
    public void mostrar()
    {
        String[] columnas={"Codigo","Nombre","Correo","Escuela"};
        Object[] obj =new Object[4];
        DefaultTableModel tabla = new DefaultTableModel(null,columnas);
        
        try 
        {
            List lista =daoC.mostrarCoordinador();
            for (int i = 0; i < lista.size(); i++) 
            {
                cor=(CoordinadorSSE)lista.get(i);
                obj[0]=cor.getIdCoordinador();
                obj[1]=cor.getNombre();
                obj[2]=cor.getCorreo();
                obj[3]=daoE.getidEscuela(cor.getIdEscuela()).getNombre();
                tabla.addRow(obj);
            }
            this.jTablaDatos.setModel(tabla);
        }
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, "Error al mostrar datos en tabla ", "Carga Fallida "
                    + e.getMessage(),
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    

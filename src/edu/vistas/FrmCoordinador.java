    public void llenarTabla()
    {
        int index = this.jTablaDatos.getSelectedRow();

        if (index > -1) 
        {
         
            this.jTxtCodigo.setText(String.valueOf(this.jTablaDatos.getValueAt(index, 0)));
            this.jTxtNombre.setText(String.valueOf(this.jTablaDatos.getValueAt(index, 1)));
            this.jTxtCorreo.setText(String.valueOf(this.jTablaDatos.getValueAt(index, 2)));
            String escuela = String.valueOf(this.jTablaDatos.getValueAt(index, 3));
            jCmbEscuela.getModel().setSelectedItem(escuela);
     

        }
     }
    private void insertar(CoordinadorSSE cor)
    {
        try 
        {      
            daoC.insertarCoordinador(cor);
            JOptionPane.showMessageDialog(null, "Coordinador Guardado Exitosamente", "Actualizacion Correcta",
                    JOptionPane.INFORMATION_MESSAGE);
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,"Ocurrio un error guardando " + e.getMessage(),
                    "Guardado Fallido",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void modificarCoordinador(CoordinadorSSE cor) 
    {
        int SioNo = JOptionPane.showConfirmDialog(this, "¿Desea modificar el registro seleccionado? ", "Modificar Empleado", 
                JOptionPane.YES_NO_OPTION);

        if (SioNo == 0) 
        {
            daoC.modificarCoordinador(cor);
            JOptionPane.showMessageDialog(null, "Coordinador Modificado Exitosamente", "Actualizacion Correcta", 
                    JOptionPane.INFORMATION_MESSAGE);
            limpiar();
            mostrar();
        }
    }

